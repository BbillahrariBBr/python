number1 = input("enter a integer number and press enter: ")
number2 = input("enter another integer number and press enter: ")
number1 = int(number1)
number2 = int(number2)
print(number1, "+", number2," = ", number1+number2)
print(number1, "-", number2," = ", number1-number2)
print(number1, "x", number2," = ", number1*number2)
print(number1, "/", number2," = ", number1/number2)
print(number1, "%", number2," = ", number1%number2)
print(number1, "//", number2," = ", number1//number2)
print(number1, "**", number2," = ", number1**number2)
